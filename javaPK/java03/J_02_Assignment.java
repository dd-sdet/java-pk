package java03;

public class J_02_Assignment {

	public static void main(String[] args) {
		int a = 13, b = 10;

		System.out.println(a += b); // a = 23
		System.out.println(a -= b); // a = 13
		System.out.println(a *= b); // a = 130
		System.out.println(a /= b); // a = 13
		System.out.println(a %= b); // a = 3
	}

}
