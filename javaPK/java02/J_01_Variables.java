package java02;

public class J_01_Variables {

	public static void main(String[] args) {
		int a;
		a = 10;
		System.out.println(a);

		int b = 20;
		System.out.println(b);

		int c, d;
		c = 30;
		d = 40;
		System.out.println(c);
		System.out.println(d);

		int e = 50, f = 60;
		System.out.println(e);
		System.out.println(f);
	}

}
